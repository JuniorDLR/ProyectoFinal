    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        // TODO add your handling code here:
        this.BtnGuardar.setEnabled(false);
        listaU.add(new Usuario(
                
                Integer.parseInt(this.TFCod.getText()),
                this.tfUserName.getText(),
                this.tfPw.getText(),
                this.TfNombres.getText(),
                this.TfApellidos.getText(),
                0
        ));
        JOptionPane.showMessageDialog(this, "Registro Guardado...", "La Raya", JOptionPane.INFORMATION_MESSAGE);
        this.mostrarTabla();
        this.BtnGuardar.setEnabled(true);
    }//GEN-LAST:event_BtnGuardarActionPerformed

    private void btnAntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAntActionPerformed
        // TODO add your handling code here:
        if(fila ==0 ) fila = listaU.size()-1;
        else fila--;
        this.ubicarTexto();
    }//GEN-LAST:event_btnAntActionPerformed

    private void btnSigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSigActionPerformed
        // TODO add your handling code here:
        if(fila == (listaU.size()-1)) fila =0;
        else fila++;
        this.ubicarTexto();
    }//GEN-LAST:event_btnSigActionPerformed

    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarActionPerformed
        // TODO add your handling code here:
        this.BtnEditar.setEnabled(false);
        listaU.get(fila).setPw(this.tfPw.getText());
        listaU.get(fila).setNombres(this.TfNombres.getText());
        listaU.get(fila).setApellidos(this.TfApellidos.getText());
        listaU.get(fila).setEstado(2);
        JOptionPane.showMessageDialog(this, "Registro Guardado...", "La Raya", JOptionPane.INFORMATION_MESSAGE);
        this.mostrarTabla();
        this.BtnEditar.setEnabled(true);
        
    }//GEN-LAST:event_BtnEditarActionPerformed

