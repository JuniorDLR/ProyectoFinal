    public void mostrarTabla(){
        try{
            if(this.tbl.getRowCount()>0){
                this.tbl.setRowCount(0);
            }
            String titulos[]={"C√≥digo", "usuario", "contrase√±a", "nombre","Apellido"};
            this.tbl.setColumnIdentifiers(titulos);
            
            for(Usuario u : listaU){
                Object[] datos = new Object[5];
                datos[0]= u.getId();
                datos[1]= u.getUserName();
                datos[2]=u.getPw();
                datos[3]= u.getNombres();
                datos[4]= u.getApellidos();
                tbl.addRow(datos);
            }
            this.TblRegistros.setModel(tbl);
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public List<Usuario> getListaU() {
        return listaU;
    }

    public void setListaU(List<Usuario> listaU) {
        this.listaU = listaU;
    }
    
    private void ubicarTexto(){
        Usuario u = listaU.get(fila);
        this.TFCod.setText(""+u.getId());
        this.tfUserName.setText(u.getUserName());
        this.tfPw.setText(u.getUserName());
        this.TfNombres.setText(u.getNombres());
        this.TfApellidos.setText(u.getApellidos());
    }

    public void filtrar(){
        int columna = this.CmbCampo.getSelectedIndex();
        filtro.setRowFilter(RowFilter.regexFilter("(?i)"+ this.TfValor.getText(), columna));
    }
    
    private Usuario buscarPorId(int id){
        Usuario user = new Usuario();
        for(Usuario u : listaU){
            if(u.verificarCodigo(id)){
                user = u;
                return user;
            }
        }
        return user;
    }
    
