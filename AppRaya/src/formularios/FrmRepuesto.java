     public void mostrarTablaR(){
        try{
            if(this.tblr.getRowCount()>0){
                this.tblr.setRowCount(0);
            }
            String titulos[]={"Codigo", "Nombre" ,"Descripcion","Precio","Existencia"};
            this.tblr.setColumnIdentifiers(titulos);
            
            for(Repuesto R : listaR){
                Object[] datos = new Object[4];
                datos[0]= R.getId();
                datos[1]= R.getNombreRepuesto();
                datos[2]= R.getDescripcion();
                datos[3]= R.getPrecio();
                tblr.addRow(datos);
            }
            this.tblRegistroR.setModel(tblr);
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
